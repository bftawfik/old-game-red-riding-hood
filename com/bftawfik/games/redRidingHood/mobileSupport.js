function checkIfMobilePhone(){
  isMobilePhone = /Android|webOS|iPhone|iPad|iPod|BlackBerry|IEMobile|Opera Mini/i.test(navigator.userAgent);
  return isMobilePhone;
}
//---------------------------------------------------------------
function checkIfIOS(){
  isIOS = /iPhone|iPad|iPod/i.test(navigator.userAgent);
  return isIOS;
}
